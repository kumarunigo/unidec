
public class NamingConventions {
	
	public static void main(String args[]) {
			int a; // declaration statement
			a = 20; // initialization 
			a = 30; // reassignment
		// reserved key words
			String abc1$; // numbers, alphabets, $ , _
			String iAmOk; // camel case for multiple words
			System.out.println( "welcome to java ");
	}
	
	{
		int a=30;
	}
}
